/* 1) Number: Rappresenta i dati numerici in tutte le loro forme, sia interi che decimali.
   2) String: Rappresente i dati con una sequenza di caratteri. Deve essere delimitato tra apici singoli '' o doppi ""
   3) Boolean: Rappresenta un valore di verità, binario, che puà essere solo o "true" o "false" (vero o falso)
   4) Null: Rappresenta l'assenza di valore intenzionale. Quuindi è un tipo di dato che avrà sempre il valore "null"
   5) Undenfined: Rappresenta una variabile alla quale non è stato assegnato alcun valore.
   6) Object: Una o piu coppie Nome/valore, dove il valore può essere qualsiasi tipo di dato.
   7) Array: Rappresenta una lista ordinata di elementi, dove ogni elemento può essere qualsiasi tipo di dato.
   8) Symbol: Tipo di dato immutabile, può essere utilizzato come identificatore unico.
   9) Function: una funzione è un insieme di istruzioni racchiuse in un blocco di codice, che può essere contraddistinto da un nome,
    può accettare argomenti o parametri di ingresso e restituire dei valori.

   */

